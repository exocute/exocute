	public static void createByteArrayStringFromJarFile(String byteArrayVariableName, String jarFileName) throws Exception {
		FileInputStream arrayInputStream = new FileInputStream(jarFileName);

		byte b[] = new byte[1];
		int count = 0;
		int readLen = 0;
		boolean first = true;
		
		System.out.println("private byte[] " + byteArrayVariableName + " = new byte[] {");
		

		do {
			
			
			if (count%15 == 0) {
				System.out.println();
			}
			
			readLen = arrayInputStream.read(b);
			
			if (readLen!=-1) {
				if (!first) {
					System.out.print(", ");
				}
				first=false;
				count++;
				System.out.print((byte) b[0]);
			}
		} while (readLen != -1);

		System.out.println("};");

		System.out.println("Bytes processed:" + count);
	}

